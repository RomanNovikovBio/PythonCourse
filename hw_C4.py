a, b, c = map(int, input('Введите три числа через пробелы : ').split())
from math import sqrt
D = b**2 - 4*a*c
if D < 0:
    print('Нет корней')
if D == 0:
    x = (-b)/(2*a)
    print(f'Корень квадратного уравнения = {x}')
if D > 0:
    x_1 = ((-b)-sqrt(D))/(2*a)
    x_2 = ((-b)+sqrt(D))/(2*a)
    print(f'1 корень квадратного уравнения = {x_1}' , f'2 корень квадратного уравнения = {x_2}' , sep ='\n')
